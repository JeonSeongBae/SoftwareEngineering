public class tableListAdapter extends BaseAdapter {

    private ArrayList<ListItem> listViewItemList = new ArrayList<ListItem>() ;

    // ListViewAdapter의 생성자
    public tableListAdapter() {

    }
    public void clear(){
        listViewItemList.clear();
    }
    // Adapter에 사용되는 데이터의 개수를 리턴. : 필수 구현
    @Override
    public int getCount() {
        return listViewItemList.size() ;
    }

    // position에 위치한 데이터를 화면에 출력하는데 사용될 View를 리턴. : 필수 구현
    @Override
    public View getView(final int position, View convertView, ViewGroup parent) {
        final Context context = parent.getContext();
        final ListItem listViewItem = listViewItemList.get(position);

        // "listview_item" Layout을 inflate하여 convertView 참조 획득.
        if (convertView == null) {
            LayoutInflater inflater = (LayoutInflater) context.getSystemService(Context.LAYOUT_INFLATER_SERVICE);
            convertView = inflater.inflate(R.layout.tablelistview_item, parent, false);
        }

        // 화면에 표시될 View(Layout이 inflate된)으로부터 위젯에 대한 참조 획득
        TextView menu = (TextView) convertView.findViewById(R.id.menutext) ;
        TextView count = (TextView) convertView.findViewById(R.id.counttext) ;
        TextView price = (TextView)convertView.findViewById(R.id.pricetext);

        // 아이템 내 각 위젯에 데이터 반영
        menu.setText(listViewItem.getMenu());
        count.setText(listViewItem.getCount() + "");
        price.setText(listViewItem.getPrice()+"");

        return convertView;
    }

    // 지정한 위치(position)에 있는 데이터와 관계된 아이템(row)의 ID를 리턴. : 필수 구현
    @Override
    public long getItemId(int position) {
        return position ;
    }

    // 지정한 위치(position)에 있는 데이터 리턴 : 필수 구현
    @Override
    public Object getItem(int position) {
        return listViewItemList.get(position) ;
    }

    // 아이템 데이터 추가를 위한 함수. 개발자가 원하는대로 작성 가능.
    public void addItem(ListItem listItem) {
        listViewItemList.add(listItem);
    }
}

